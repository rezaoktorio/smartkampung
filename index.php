&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The Sustainable Development Goals yang merupakan agenda dunia pembangunan untuk kemaslahatan manusia dan planet bumi, yang mana disepakati oleh Negara anggota dalam pengimplementasian secara terpadu pada Negara masing-masing. Indonesia adalah salah satu dari negara dunia yang ikut berpartisipasi dalam penerapan program SDGs. Indonesia optimis siap untuk mengimplementasikan SDGs sesuai dengan kesepakatan bersama. Indonesia termasuk negara yang potensial untuk mengimplementasikan program SDGs ini, salah satunya dilihat berdasarkan pertumbuhan penduduknya serta indeks pembangunan manusia terhadap permukiman.
Sejalan dengan poin SDGs nomor 11 yaitu kota dan permukiman yang inklusif, aman, berdaya tahan, dan berkelanjutan. Keseriusan Indonesia dalam mewujudkan SDGs nomor 11 ini telah disepakati yang mana difasilitasi oleh Bappenas dalam mengeluarkan Konvergensi Agenda Pembangunan: Nawa Cita, RPJMN, dan SDGs pada Tahun 2015. Pembangunan berkelanjutan ini tidak bisa dipisahkan dari konsep kota cerdas atau smart city.
<br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Konsep kota cerdas ini sebagai upaya penerapan dari pembangunan berkelanjutan. Kota cerdas merupakan konsep kota yang menggunakan pemanfaatan teknologi informasi dan komunikasi (TIK) untuk menghubungkan, untuk pemantauan dan pengendalian berbagai sumber daya yang ada di dalam kota secara efektif dan efisien dalam rangka memaksimalkan pelayanan kepada warga kota.
Saat ini konsep smart city secara umum memiliki banyak kriteria, namun dalam menerapkan kriteria tersebut diperlukan penyesuaian terhadap kondisi kota. Dimana konsep smart city sendiri tidak dapat diartikan secara universal, dikarenakan pengertiannya kembali menyesuaikan dengan karakteristik kota. Eksplorasi kriteria konsep smart city yang kontekstual dengan kota Surabaya terutama dibidang permukiman menjadi fokusan dalam penelitian ini.
Penelitian ini mengunakan teori perumahan dan permukiman, serta kota cerdas yang diadaptasi sesuai kebutuhan penelitian yaitu terhadap permukiman.

